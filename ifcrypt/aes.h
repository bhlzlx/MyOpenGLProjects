#define CALCELEMENT( N1, N2, N3, N4, M1, M2, M3, M4) GFPLUS( GFPLUS( GFPLUS( GFMULT##N1(M1) ,  GFMULT##N2(M2) ),GFMULT##N3(M3) ), GFMULT##N4(M4))

	struct AesChunk
	{
		union
		{
			uint8_t data[16];
			struct
			{
				uint8_t
					b0, b1, b2, b3,
					b4, b5, b6, b7,
					b8, b9, ba, bb,
					bc, bd, be, bf;
			};
			struct
			{
				uint16_t
					w0, w1,
					w2, w3,
					w4, w5,
					w6, w7;
			};
			struct
			{
				uint32_t
					dw0, dw1,
					dw2, dw3;
			};
			struct
			{
				uint64_t i64[2];
			};
		};
	};

#ifdef __cplusplus
	static_assert(sizeof(AesChunk) == 16, "AesChunk size does not match!");
#endif

	inline size_t AlignFor16Bytes(size_t _size)
	{
		return (_size + 15)&~(15);
	}

	inline void SubBytes(AesChunk * _chunk)
	{
		for (int i = 0; i < 16; ++i)
		{
			_chunk->data[i] = sBox[_chunk->data[i]];
		}
	}

	inline void SubBytesInv(AesChunk * _chunk)
	{
		for (int i = 0; i < 16; ++i)
		{
			_chunk->data[i] = sBoxInv[_chunk->data[i]];
		}
	}

	inline void ShiftRows(AesChunk* _chunk)
	{
		uint8_t b = 0;
		// shift 2nd row
		b = _chunk->b1;
		_chunk->b1 = _chunk->b5; _chunk->b5 = _chunk->b9; _chunk->b9 = _chunk->bd; _chunk->bd = b;
		// shift 3rd row
		b = _chunk->b2; _chunk->b2 = _chunk->ba; _chunk->ba = b;
		b = _chunk->b6; _chunk->b6 = _chunk->be; _chunk->be = b;
		// shift 4th row
		b = _chunk->bf;
		_chunk->bf = _chunk->bb; _chunk->bb = _chunk->b7; _chunk->b7 = _chunk->b3; _chunk->b3 = b;
	}

	inline void ShiftRowsInv(AesChunk* _chunk)
	{
		uint8_t b = 0;
		// shift 2nd row
		b = _chunk->bd;
		_chunk->bd = _chunk->b9; _chunk->b9 = _chunk->b5; _chunk->b5 = _chunk->b1; _chunk->b1 = b;
		// shift 3rd row
		b = _chunk->b2; _chunk->b2 = _chunk->ba; _chunk->ba = b;
		b = _chunk->b6; _chunk->b6 = _chunk->be; _chunk->be = b;
		// shift 4th row
		b = _chunk->b3;
		_chunk->b3 = _chunk->b7; _chunk->b7 = _chunk->bb; _chunk->bb = _chunk->bf; _chunk->bf = b;
	}

	inline void MixColumns(AesChunk * _chunk)
	{
		uint8_t v[4];
		for (int i = 0; i<4; ++i)
		{
			v[0] = CALCELEMENT(2, 3, 1, 1, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			v[1] = CALCELEMENT(1, 2, 3, 1, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			v[2] = CALCELEMENT(1, 1, 2, 3, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			v[3] = CALCELEMENT(3, 1, 1, 2, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			memcpy(_chunk->data + i * 4, v, sizeof(v));
		}
	}

	inline void MixColumnsInv(AesChunk * _chunk)
	{
		uint8_t v[4];
		for (int i = 0; i<4; ++i)
		{
			v[0] = CALCELEMENT(e, b, d, 9, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			v[1] = CALCELEMENT(9, e, b, d, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			v[2] = CALCELEMENT(d, 9, e, b, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			v[3] = CALCELEMENT(b, d, 9, e, _chunk->data[i * 4], _chunk->data[1 + i * 4], _chunk->data[2 + i * 4], _chunk->data[3 + i * 4]);
			memcpy(_chunk->data + i * 4, v, sizeof(v));
		}
	}

	inline void AddRoundKey(AesChunk * _data, const AesChunk* _wkey)
	{
		_data->dw0 ^= _wkey->dw0;
		_data->dw1 ^= _wkey->dw1;
		_data->dw2 ^= _wkey->dw2;
		_data->dw3 ^= _wkey->dw3;
	}

	inline void ExtendKey(AesChunk(&_wkey)[11])
	{
		uint32_t v[2];
		for (uint32_t i = 1; i<11; ++i)
		{
			v[0] = _wkey[i - 1].dw3;
			v[0] = v[0] << 24 | v[0] >> 8;
			uint8_t* pb = (uint8_t*)&v[0];
			for (uint32_t j = 0; j<4; ++j)
			{
				pb[j] = sBox[pb[j]];
			}
			v[1] = _wkey[i - 1].dw0;
			_wkey[i].dw0 = v[0] ^ v[1] ^ Rcon[i - 1];
			_wkey[i].dw1 = _wkey[i - 1].dw1 ^ _wkey[i].dw0;
			_wkey[i].dw2 = _wkey[i - 1].dw2 ^ _wkey[i].dw1;
			_wkey[i].dw3 = _wkey[i - 1].dw3 ^ _wkey[i].dw2;
		}
	}

	inline void Encrypt(AesChunk* _chunk, const AesChunk(&_wkey)[11])
	{
		AddRoundKey(_chunk, &_wkey[0]);
		for (int i = 1; i < 10; ++i)
		{
			SubBytes(_chunk);
			ShiftRows(_chunk);
			MixColumns(_chunk);
			AddRoundKey(_chunk, &_wkey[i]);
		}
		SubBytes(_chunk);
		ShiftRows(_chunk);
		AddRoundKey(_chunk, &_wkey[10]);
	}

	inline void Decrypt(AesChunk* _chunk, const AesChunk(&_wkey)[11])
	{
		AddRoundKey(_chunk, &_wkey[10]);
		for (int i = 1; i < 10; ++i)
		{
			ShiftRowsInv(_chunk);
			SubBytesInv(_chunk);
			AddRoundKey(_chunk, &_wkey[10 - i]);
			MixColumnsInv(_chunk);
		}
		ShiftRowsInv(_chunk);
		SubBytesInv(_chunk);
		AddRoundKey(_chunk, &_wkey[0]);
	}

	inline size_t Encrypt_CBC_PKCS5Padding(const void * _data, size_t _size, const AesChunk * _xor, const AesChunk(&_wkey)[11], std::vector<AesChunk>& _encrypt)
	{
		size_t fillsize = AlignFor16Bytes(_size);
		size_t padsize = _size & 15;
		size_t nChunks = fillsize >> 4; // 除16等于右移四
		_encrypt.clear();
		const AesChunk * chunks = (AesChunk*)_data;
		if (!padsize)
		{
			_encrypt.resize(nChunks);
			memcpy(&_encrypt[0], _data, _size);
			memset(&(*_encrypt.rbegin()), 16, 16);
		}
		else
		{
			_encrypt.resize(nChunks);
			memset(&(*_encrypt.rbegin()), padsize, 16);
			memcpy(&_encrypt[0], _data, _size);
		}
		// 填充完了，加密
		for (size_t i = 0; i < nChunks; ++i)
		{
			// xor
			_encrypt[i].i64[0] ^= _xor->i64[0];
			_encrypt[i].i64[1] ^= _xor->i64[1];
			// encrypt
			Encrypt(&_encrypt[i], _wkey);
			// next xor vector
			_xor = &_encrypt[i];
		}
		return fillsize;
	}

	inline size_t Decrypt_CBC_PKCS5Padding(const void * _data, size_t _size, const AesChunk * _xor, const AesChunk(&_wkey)[11], std::vector<AesChunk>& _decrypt)
	{
		AesChunk * chunks = (AesChunk*)_data;
		size_t nchunk = _size >> 4;
		_decrypt.clear();
		_decrypt.resize(nchunk);
		memcpy(&_decrypt[0], chunks, nchunk << 4);
		const AesChunk* xor = _xor;
		for (size_t i = 0; i < nchunk; ++i)
		{
			// decrypt
			Decrypt(&_decrypt[i], _wkey);
			// xor
			_decrypt[i].i64[0] ^= xor->i64[0];
			_decrypt[i].i64[1] ^= xor->i64[1];
			// next xor vector
			xor = &chunks[i];
		}
		uint8_t padsize = _decrypt.rbegin()->bf;
		return _size - padsize;
	}
